/** 支持对话的 10 位核心人物姓名（与后端 seed name 一致） */
export const CORE_FIGURES_WITH_DIALOGUE = [
  '罗辑',
  '叶文洁',
  '章北海',
  '程心',
  '托马斯·维德',
  '史强',
  '汪淼',
  '丁仪',
  '云天明',
  '智子 (仿生人)',
] as const

export type CoreFigureName = (typeof CORE_FIGURES_WITH_DIALOGUE)[number]

export function hasDialogue(figureName: string): figureName is CoreFigureName {
  return (CORE_FIGURES_WITH_DIALOGUE as readonly string[]).includes(figureName)
}

